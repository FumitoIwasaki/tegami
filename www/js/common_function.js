function fnJuchu_kubetu_mei_get(rosai_cd){
    
    var item = null;
    
    if (rosai_cd=="1") {
      item = "元請受注";
    }
    
    if (rosai_cd=="2") {
      item = "下請受注";
    }
    

    return item;
    
}




