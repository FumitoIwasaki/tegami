//*****************************************************************************
//現場労災表示
//*****************************************************************************
function fnRosai_mei_get(rosai_cd){
    
    var item = null;
    
    if (rosai_cd=="1") {
      item = "当社にて加入";
    }
    
    if (rosai_cd=="2") {
      item = "他社にて加入";
    }
    
    if (rosai_cd=="3") {
      item = "ジョブ単位で当社単独加入";
    }
    
    return item;
    
}

